"""Shared definitions for the synthetic MedArchive sample data.

`SERVICES` is a realistic catalog of ~80 Russian-language medical services
spanning every category the system understands. Each entry carries:
  * ``name``     — the official catalog service name (canonical),
  * ``synonyms`` — 2-5 aliases: Russian abbreviations (ОАК/ОАМ/ЭКГ/УЗИ ОБП),
                   English variants (CBC), and at least one common misspelling,
  * ``category`` — one of the spec categories,
  * ``icd_code`` — optional ICD-10/МКБ-10 code,
  * ``base``     — a plausible resident KZT price used to seed price lists.

`CLINICS` is a small set of Kazakhstan clinics with full header metadata
(name, city, address, 12-digit BIN, +7 phone, email).

The price-list generators draw service names/synonyms from here so that every
emitted row is matchable against the catalog (normalization succeeds), while a
handful of deliberately off-catalog rows feed the unmatched queue.
"""
from __future__ import annotations

from dataclasses import dataclass, field


